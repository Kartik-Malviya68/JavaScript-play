const fruit = "mango";

switch (fruit) {
    case 'apple' :
        console.log("red apple");
        break;
    case "banana" : 
        console.log("banana is yellow");  
        break;
    case "mango" : 
        console.log("mango is yellow");  
        break;
    case "orange" : 
        console.log("orange is orange");  
        break;
    default : 
        console.log("default value");  
        break;
}